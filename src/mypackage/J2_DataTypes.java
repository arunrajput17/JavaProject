package mypackage;

public class J2_DataTypes {

	public static void main(String[] args) {
		
		// Primitive data types like int, double, char, boolean, sort, float etc
		int a =10;
		double d=20.54545;
		char c='H';
		boolean b = true;
		float f = 10;
		
		// Derived Data types like String, Array, ArrayList, HashMap etc..
		String s = "welcome";
		
		System.out.println(a);
		System.out.println(d);
		System.out.println(c);
		System.out.println(b);
		System.out.println(f);
		System.out.println(s);

	}

}
