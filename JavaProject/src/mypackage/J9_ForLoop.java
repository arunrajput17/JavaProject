package mypackage;

public class J9_ForLoop {

	public static void main(String[] args) 
	{
		
		// Print 1 to 10 numbers
		System.out.println("Print 1 to 10 numbers");
		for (int i=1; i<=10; i++)
		{
			System.out.println(i);
		}
		
		//Print 1 to 10 numbers in descending order
		System.out.println("Print 1 to 10 numbers in descending order");
		for(int i=10; i>0; i--)
		{
			System.out.println(i);
		}
		

	}

}
