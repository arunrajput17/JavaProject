package mypackage;

public class J7_WhileLoop {

	public static void main(String[] args) 
	{
		
		System.out.println("print 1 to 10");
		int i =1;
		
		// print 1 to 10
		while (i<=10) 
		{
			System.out.println(i);
			i++;
		}
		
		
		// Print even number between 1 to 10
		System.out.println("Print even number between 1 to 10");
		int j = 2;
		while (j<=10)
		{
			System.out.println(j);
			j+=2;
		}
		
		// Print odd number between 1 to 10
		System.out.println("Print odd number between 1 to 10");
		int k = 1;
		while (k<=10)
		{
			System.out.println(k);
			k+=2;
		}
		
		
		// Print number in descending order from 10 to 1
		System.out.println("Print number in descending order from 10 to 1");
		int m = 10;
		while (m>0)
		{
			System.out.println(m);
			m--;
		}

	}

}
